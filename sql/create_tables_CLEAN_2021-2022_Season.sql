-- SQL statements to create needed tables in existing PostgreSQL DB

CREATE TABLE games (
    id serial PRIMARY KEY,
    week_num smallint NOT NULL,
    gamedate date DEFAULT NULL,
    team_id smallint NOT NULL,
    opponent_id smallint DEFAULT NULL,
    tiebreaker smallint DEFAULT 0,
    tiebreakerpts smallint DEFAULT 0,
    locale smallint DEFAULT 0,
    outcome smallint DEFAULT 0
);

comment on column games.week_num is 'week # of game';
comment on column games.gamedate is 'date of game';
comment on column games.team_id is 'player team id';
comment on column games.opponent_id is 'game opponent team id';
comment on column games.tiebreaker is '=1 if game is tiebreaker game for week';
comment on column games.tiebreakerpts is '# actual total points for tiebreaker game';
comment on column games.locale is 'Value of 0-3 for No game, Home, Away, Neutral';
comment on column games.outcome is 'indicates winner of game (0=no decision; 1=Team; 2=Opponent)';

CREATE TABLE picks (
    id serial PRIMARY KEY,
    player_id smallint NOT NULL,
    game_id int NOT NULL,
    pick smallint DEFAULT 0,
    pick_result smallint DEFAULT 0,
    tiebreakerpts smallint DEFAULT 0
);

comment on column picks.player_id is 'reference to player table';
comment on column picks.game_id is 'reference to games table';
comment on column picks.pick is 'indicates picked winner of game (0=no pick; 1=Team; 2=Opponent)';
comment on column picks.pick_result is 'tracks whether pick is correct (1=correct, 0=wrong)';
comment on column picks.tiebreakerpts is '# total points selected for tiebreaker game';

CREATE TABLE player (
  id serial PRIMARY KEY,
  email varchar(50) NOT NULL,
  firstname varchar(50) NOT NULL,
  lastname varchar(50) NOT NULL,
  "password" varchar(255) DEFAULT NULL,
  team_id int NOT NULL,
  num_wins smallint DEFAULT 0,
  correct_picks smallint DEFAULT 0,
  wrong_picks smallint DEFAULT 0,
  createdate date NOT NULL,
  lastlogindate timestamp DEFAULT NULL
);

comment on column player.team_id is 'team that player selected';
comment on column player.num_wins is 'number of wins predicted for team selected';
comment on column player.correct_picks is 'overall correct # of games picked';
comment on column player.wrong_picks is 'overall incorrect # of games picked';


CREATE TABLE player_login (
  id serial PRIMARY KEY,
  player_id int NOT NULL,
  login_date timestamp NOT NULL
);

CREATE TABLE teams (
    id serial PRIMARY KEY,
    team_name varchar(255) NOT NULL,
    school varchar(255) NOT NULL,
    city varchar(50) NOT NULL,
    state varchar(50) NOT NULL,
    conference varchar(255) NOT NULL
);

CREATE TABLE weeks (
  id serial PRIMARY KEY,
  num smallint NOT NULL,
  "name" varchar(10) NOT NULL,
  from_date date NOT NULL,
  to_date date NOT NULL,
  deadline_date date NOT NULL,
  num_games smallint NOT NULL
);

comment on column weeks.from_date is 'date on which week of games begins, typically a Monday';
comment on column weeks.to_date is 'date on which week of games ends, typically a Sunday';
comment on column weeks.deadline_date is 'date by which players must submit picks, after this date Edit button hidden';
comment on column weeks.num_games is 'number of active games for week, calculated in games.php after admin updates games';


CREATE TABLE weeks_won (
  id serial PRIMARY KEY,
  week_id int NOT NULL,
  player_id int NOT NULL,
  week_correct_picks smallint DEFAULT 0,
  week_wrong_picks smallint DEFAULT 0,
  week_result smallint NOT NULL,
  money_result smallint DEFAULT 0
);

comment on column weeks_won.week_id is 'week num from weeks table';
comment on column weeks_won.player_id is 'played id from player table';
comment on column weeks_won.week_correct_picks is 'number of correct picks made that week';
comment on column weeks_won.week_wrong_picks is 'number of incorrect picks made that week';
comment on column weeks_won.week_result is 'Player result for week (0=lost, 1=won, 2=tied)';
comment on column weeks_won.money_result is 'if player won $ (1=won; 0=lost)';

CREATE TABLE winners (
  id serial PRIMARY KEY,
  week_id int NOT NULL,
  player_id int NOT NULL
);

comment on column winners.week_id is 'week num from weeks table';
comment on column winners.player_id is 'winner, id from player table';

